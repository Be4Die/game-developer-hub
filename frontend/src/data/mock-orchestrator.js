// Генератор моковых логов
export function generateMockLogEntries(count = 20) {
  const sources = ['stdout', 'stderr']
  const messages = [
    'Server started on port 8080',
    'WebSocket connection accepted from 10.0.0.1',
    'Player connected: id=42, name="PlayerOne"',
    'Player disconnected: id=42, reason="timeout"',
    'Game round started: map=europe_2, players=12',
    'Game round ended: winner=team_a, duration=180s',
    'Heartbeat OK',
    'Memory usage: 256MB / 512MB',
    'WARNING: High CPU usage detected (85%)',
    'ERROR: Failed to connect to database, retrying...',
    'Connection pool: active=5, idle=10',
    'Room created: id=room-782, max_players=24',
    'Room destroyed: id=room-551, reason="empty"',
    'Ping statistics: avg=45ms, max=120ms',
    'Checkpoint saved: state_v=1042',
    'Rate limiter: 42 requests/min (limit: 100)',
    'ERROR: Out of memory, attempting GC',
    'GC completed: freed 128MB',
    'Config reloaded successfully',
    'Metrics exported to collector',
  ]
  const entries = []
  const now = Date.now()
  for (let i = 0; i < count; i++) {
    entries.push({
      timestamp: new Date(now - (count - i) * 3000).toISOString(),
      source: sources[Math.floor(Math.random() * sources.length)],
      message: messages[Math.floor(Math.random() * messages.length)],
    })
  }
  return entries
}
